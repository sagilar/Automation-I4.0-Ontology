    private void writeToText(String message) {
        message = message.replace("\n", "\r\n");
        PrintWriter out = null;
        try {
            File file = new File("inferences_xppu.txt");
            if (!file.exists()) {
                file.createNewFile();
            }
            out = new PrintWriter(new FileWriter("inferences_xppu.txt", true));
            out.append(message);

            JOptionPane.showMessageDialog(null, "The file has been saved", "Inference results save correctly", JOptionPane.INFORMATION_MESSAGE);

            Desktop desktop = Desktop.getDesktop();
            if (desktop.isSupported(Desktop.Action.OPEN)) {
                desktop.open(file);
            }

        } catch (IOException ex) {
        } finally {
            try {
                out.close();
            } catch (NullPointerException ex) {

            }

        }
    }

